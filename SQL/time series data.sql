select avg(salesAmount) as totalSales from salesdata;

select date , salesAmount from salesData where salesAmount = (
	select max(salesAmount) from salesData
);

select date , salesAmount , avg(salesAmount) over (order by date rows between 6 preceding and current row) as
	rolling_average from salesData;
    
select week(date) as weeknumber,
	sum(salesAmount) as weeklysales from salesData
    group by week(date)
    order by weeknumber;
    
select month(date) as monthnumber,
	sum(salesAmount) as monthlysales from salesData
    group by month(date)
    order by monthnumber;
